public class Ex01_출력 {
  public static void main(String[] args) {
    // 괄호안의 내용을 출력하고 줄바꿈 수행하기 
    System.out.println("j");
    System.out.println("a");
    System.out.println("v");
    System.out.println("a");

    //아무런 내용도 출력하지 않고 줄바꿈만 수행
    System.out.println();

    // 괄호안의 내용을 출력하고 출바꿈 안하기
    System.out.println("j");
    System.out.println("a");
    System.out.println("v");
    System.out.println("a");
  }
}
