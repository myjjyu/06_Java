public class Ex06_IfElse {
  public static void main(String[] args) {
    boolean isKorean = true;

    if(isKorean) {
      System.out.println("학국사람입니다 ---(1)");
    }else{
      System.out.println("한국사람이 아닙니다 ---(2)");
    }

    if(!isKorean) {
      System.out.println("한국사람이 아닙니다 ---(3)");
    }else{
      System.out.println("학국사람입니다 ---(4)");
    }

  }
}
